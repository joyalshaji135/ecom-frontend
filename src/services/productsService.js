export const productsService = {
  async getAllProducts() {
    // Simulate API call
    return new Promise((resolve) => {
      setTimeout(() => {
        resolve(fashionProducts);
      }, 500);
    });
  },

  async getProductById(id) {
    return new Promise((resolve, reject) => {
      setTimeout(() => {
        const product = fashionProducts.find(p => p.id === parseInt(id));
        if (product) {
          resolve(product);
        } else {
          reject(new Error('Product not found'));
        }
      }, 500);
    });
  },

  async getProductsByCategory(category) {
    return new Promise((resolve) => {
      setTimeout(() => {
        const products = fashionProducts.filter(p => 
          p.category.toLowerCase() === category.toLowerCase()
        );
        resolve(products);
      }, 500);
    });
  },

  async getSimilarProducts(productId, limit = 4) {
    return new Promise((resolve) => {
      setTimeout(() => {
        const product = fashionProducts.find(p => p.id === parseInt(productId));
        if (!product) {
          resolve([]);
          return;
        }
        
        const similar = fashionProducts
          .filter(p => p.id !== parseInt(productId) && p.category === product.category)
          .slice(0, limit);
        
        resolve(similar);
      }, 500);
    });
  }
};